    /*****************************************************************
     * 📏 HAVERSINE DISTANCE (MILES)
     *****************************************************************/
    function getDistanceMiles(lat1, lon1, lat2, lon2) {
        const R = 3958.8; // Earth radius in miles
        const toRad = (deg) => deg * Math.PI / 180;

        const dLat = toRad(lat2 - lat1);
        const dLon = toRad(lon2 - lon1);

        const a =
            Math.sin(dLat / 2) ** 2 +
            Math.cos(toRad(lat1)) *
            Math.cos(toRad(lat2)) *
            Math.sin(dLon / 2) ** 2;

        const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
        return R * c;
    }

    /*****************************************************************
     * 🏪 GET CURRENT STORE
     *****************************************************************/
    function getCurrentStore() {
        const btn = document.querySelector('#storeNumber');
        if (!btn) return null;

        const store = btn.textContent.trim();
        return /^\d{4}$/.test(store) ? store : null;
    }

    /*****************************************************************
     * 🔍 EXTRACT STORE NUMBER FROM ROW TEXT
     *****************************************************************/
    function extractStoreNumber(text) {
        const match = text.match(/Store\s*-\s*(\d{4})/);
        return match ? match[1] : null;
    }

    /*****************************************************************
     * 🎯 MAIN PROCESS
     *****************************************************************/
    function processRows() {
        const currentStore = getCurrentStore();
        if (!currentStore || !STORE_COORDS[currentStore]) return;

        const origin = STORE_COORDS[currentStore];

        const spans = document.querySelectorAll('span[data-bind="text: locationDisplayName"]');

        spans.forEach(span => {
            // Prevent duplicate injections
            if (span.dataset.distanceInjected === "true") return;

            const text = span.textContent;
            const targetStore = extractStoreNumber(text);

            // Filter: must be valid + exist in dataset
            if (!targetStore || !STORE_COORDS[targetStore]) return;

            const target = STORE_COORDS[targetStore];

            const distance = getDistanceMiles(
                origin.lat,
                origin.lng,
                target.lat,
                target.lng
            );

            const rounded = distance.toFixed(1);

            // Create display element
            const distEl = document.createElement('span');
            distEl.textContent = ` • ${rounded} mi`;
            let color;

if (distance < 10) {
    color = '#28a745'; // green
} else if (distance <= 30) {
    color = '#ffc107'; // yellow
} else {
    color = '#dc3545'; // red
}

distEl.style.color = color;
            distEl.style.marginLeft = '6px';
            distEl.style.fontWeight = '500';

            span.appendChild(distEl);

            // Mark as processed
            span.dataset.distanceInjected = "true";
        });
    }

    /*****************************************************************
     * 👀 OBSERVER (HANDLES DYNAMIC UPDATES)
     *****************************************************************/
    const observer = new MutationObserver(() => {
        processRows();
    });

    function initObserver() {
        const target = document.body;

        observer.observe(target, {
            childList: true,
            subtree: true
        });
    }

    /*****************************************************************
     * 🚀 INIT
     *****************************************************************/
    function init() {
        initObserver();
        processRows();
    }

    window.addEventListener('load', init);

